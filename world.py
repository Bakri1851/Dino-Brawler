import pygame

tile_size = 50
width = 1250  # width of window
height = 800  # height of window
win = pygame.display.set_mode((width, height))  # Size of window


class World():
    def __init__(self, data):
        self.tile_list = []
        # load images
        metalTile_img = pygame.image.load(
            r"C:\Users\Bakri\PycharmProjects\Coursework\Game Assets\Map Assets\1 Tiles\IndustrialTile_25.png")
        metalBar_img = pygame.image.load(
            r"C:\Users\Bakri\PycharmProjects\Coursework\Game Assets\Map Assets\1 Tiles\IndustrialTile_27.png")

#        metalTile_img = pygame.image.load('Map Assets/1 Tiles/IndustrialTile_25.png')
#        metalBar_img = pygame.image.load('Map Assets/1 Tiles/IndustrialTile_27.png')

        row_count = 0
        for row in data:
            col_count = 0
            for tile in row:
                if tile == 1:
                    img = pygame.transform.scale(metalTile_img, (tile_size, tile_size))
                    img_rect = img.get_rect()
                    img_rect.x = col_count * tile_size
                    img_rect.y = row_count * tile_size
                    tile = (img, img_rect)
                    self.tile_list.append(tile)
                elif tile == 2:
                    img = pygame.transform.scale(metalBar_img, (tile_size, tile_size))
                    img_rect = img.get_rect()
                    img_rect.x = col_count * tile_size
                    img_rect.y = row_count * tile_size
                    tile = (img, img_rect)
                    self.tile_list.append(tile)

                col_count += 1
            row_count += 1

    def draw(self):
        for tile in self.tile_list:
            win.blit(tile[0], tile[1])


