import pygame
import random

width = 1250  # width of window
height = 800  # height of window
win = pygame.display.set_mode((width, height))  # Size of window

tile_size = 50
metal_tile_width = 50
metal_tile_height = 50
metal_bar_width = 50
metal_bar_height = 25


class World():
    def __init__(self, data):
        self.tile_list = []
        # load images
        metal_tile_img = pygame.image.load('Map Assets/1 Tiles/IndustrialTile_25.png')
        metal_bar_img = pygame.image.load('Map Assets/1 Tiles/IndustrialTile_27.png')

        row_count = 0
        for row in data:
            col_count = 0
            for tile in row:
                if tile == 1:
                    img = pygame.transform.scale(metal_tile_img, (tile_size, tile_size))
                    x = col_count * tile_size
                    y = row_count * tile_size
                    img_rect = pygame.Rect(x, y, metal_tile_width, metal_tile_height)
                    tile = (img, img_rect)
                    self.tile_list.append(tile)
                elif tile == 2:
                    img = pygame.transform.scale(metal_bar_img, (tile_size, tile_size))
                    x = col_count * tile_size
                    y = row_count * tile_size
                    img_rect = pygame.Rect(x, y, metal_bar_width, metal_bar_height)
                    tile = (img, img_rect)
                    self.tile_list.append(tile)

                col_count += 1
            row_count += 1

    def draw(self):
        for tile in self.tile_list:
            win.blit(tile[0], tile[1])


